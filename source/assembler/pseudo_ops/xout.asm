		.section as16code

; ************************************************************************************************
;
;								.xout does jmp $FFFF on pass 2
;
; ************************************************************************************************

AXXOutCmd:	;; {.xout}
		lda 	AXPass
		cmp 	#2
		beq		_AXXExit
		rts
_AXXExit:
		jmp 	$FFFF
		
		.send as16code
		
