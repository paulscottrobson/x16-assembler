        .section as16code

; ************************************************************************************************
;
;                                   Access identifier memory
;
; ************************************************************************************************

AXIOpen:
        pha     
        lda     #AXAPILock
        jsr     AXCallAPI
        pla
        rts

AXIClose:
        pha     
        lda     #AXAPIUnlock
        jsr     AXCallAPI
        pla
        rts

        .send as16code


