		.section as16code

; ************************************************************************************************
;
;					Work out the address mode from X , store in AXAddrMode
;						   CC if okay, CS if error with error in A.
;						     Expressions evaluated as per pass 2.
;
; ************************************************************************************************

AXIdentifyAddressMode:
		.byte 	$DB
		
		.send as16code
		
		.section as16storage
AXAddrMode:
		.fill 	1
		.send as16storage

