; ************************************************************************************************
;
;		Insert parameter A (0-n-1, one short) at position X in buffer. Parameter is obtained
;		from the *previous frame*
;
; ************************************************************************************************

AXIInsertParameter:	
		jsr 	AXIOpen 					; start.


		jsr 	AXIClose 					; close access

		rts


		.send as16code

