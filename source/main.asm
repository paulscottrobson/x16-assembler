		* = $1000
		.dsection as16code

		* = STORAGE
		.dsection as16storage

		* = ZEROPAGE
		.dsection as16zeropage

; ************************************************************************************************
;
;										Test code boot
;
; ************************************************************************************************

		.section as16code
		jmp 	Start

Start:	ldx 	#$FF
		txs
		.byte 	$DB
		bra 	Start
		.send as16code

